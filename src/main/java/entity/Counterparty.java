package com.sample;


public class Counterparty {

  private long idCounterparty;
  private String name;
  private String innNumber;


  public long getIdCounterparty() {
    return idCounterparty;
  }

  public void setIdCounterparty(long idCounterparty) {
    this.idCounterparty = idCounterparty;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getInnNumber() {
    return innNumber;
  }

  public void setInnNumber(String innNumber) {
    this.innNumber = innNumber;
  }

}
